/**
 * Render the `auto_classify/builtins/index.ts` barrel file.
 *
 * For every `kind:"builtin"` entry in the registry we emit:
 *   import { <function_name> } from "./check_<group_id>.js";
 * and collect the exports into a `Record<function_name, BuiltinCheckFn>`.
 *
 * The orchestrator dispatches builtins by `function_name` via this map.
 * Deterministic order: entries follow registry array order so two identical
 * registries render byte-identical barrels.
 */

import type { KnownIssue } from "../known_issues_types.js";

export interface BarrelEntry {
  group_id: string;
  function_name: string;
}

export function collect_barrel_entries(
  registry: readonly KnownIssue[],
): BarrelEntry[] {
  const out: BarrelEntry[] = [];
  for (const issue of registry) {
    if (issue.classifier.kind !== "builtin") continue;
    out.push({
      group_id: issue.group_id,
      function_name: issue.classifier.function_name,
    });
  }
  return out;
}

/**
 * Render a barrel file whose location is `auto_classify/builtins/index.ts`.
 * Sibling `check_<group_id>.ts` files and parent-dir type imports are resolved
 * relative to that path.
 */
export function render_builtins_barrel(registry: readonly KnownIssue[]): string {
  const entries = collect_barrel_entries(registry);
  const lines: string[] = [];
  lines.push(
    "// AUTO-GENERATED by render_builtins_barrel — do not edit by hand.",
    "// Regenerated whenever the known-issues registry mutates. The orchestrator",
    "// looks up a builtin classifier by `function_name` via BUILTIN_CHECKS.",
    "",
    "import type { EnrichedEntryPoint } from \"../../entry_point_types.js\";",
    "import type { FileLinesReader } from \"../types.js\";",
    "",
  );
  for (const entry of entries) {
    lines.push(
      `import { ${entry.function_name} } from "./check_${entry.group_id}.js";`,
    );
  }
  if (entries.length > 0) lines.push("");
  lines.push(
    "export type BuiltinCheckFn = (",
    "  entry_point: EnrichedEntryPoint,",
    "  read_file_lines: FileLinesReader,",
    ") => boolean;",
    "",
    "export const BUILTIN_CHECKS: Record<string, BuiltinCheckFn> = {",
  );
  for (const entry of entries) {
    lines.push(`  ${entry.function_name},`);
  }
  lines.push("};", "");
  return lines.join("\n");
}
